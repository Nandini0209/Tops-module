 #What are negative indexes and why are they used?

'''In Python, negative indexes are used to access elements from the end of a sequence (such as a list, tuple, or string).
Negative indexes count backward from the end of the sequence, with -1 representing the last element,
-2 representing the second-to-last element....They are particularly useful when you want to access elements relative
to the end of the sequence,such as when dealing with sequences of variable length or when you
simply want to avoid calculating the length of the sequence.'''
